a, b, x = map(int, input().split())

maxn = 10**9 + 1
minn = 0

while maxn - minn > 1:
    midn = (maxn+minn) // 2
    print(maxn, midn, minn)
    if x < a*midn + b*len(str(midn)):
        maxn = midn
    else:
        minn = midn

print(minn)

